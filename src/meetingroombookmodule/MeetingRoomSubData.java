package meetingroombookmodule;

public class MeetingRoomSubData {
	
	private String RoomName ; 
	private String RoomEmailId ; 
	private String Location ; 
	
	
	public String getRoomName() {
		return RoomName;
	}
	public void setRoomName(String roomName) {
		RoomName = roomName;
	}
	public String getRoomEmailId() {
		return RoomEmailId;
	}
	public void setRoomEmailId(String roomEmailId) {
		RoomEmailId = roomEmailId;
	}
	public String getLocation() {
		return Location;
	}
	public void setLocation(String location) {
		Location = location;
	}
	

}
